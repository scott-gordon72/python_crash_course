def make_album(artist_name, album_title):
    album = {artist_name: album_title}
    return album

album_1 = make_album('pink floyd', 'dark side of the moon')
print(album_1)
album_2 = make_album('the beatles', 'sgt. pepper lonely heart club band')
print(album_2)
album_3 = make_album('the beatles', 'abbey road')
print(album_3)
    
